############################################
############################################
library(RColorBrewer)
############################################
c <- brewer.pal(9, "Set1");
############################################
x <- seq(0,100,1);
############################################
A <- x;
A <- as.matrix(A);
A[,] <- 1.5;
############################################
B = 8;
############################################
C = 0.05;
############################################
matplot(x, B/x, bg = c[1], pch = 21, xlab = "Flow rate [mL/min]", ylab = "HETP [mm]", axes = F)
matplot(x, A, bg = c[2], pch = 21, add = T)
matplot(x, C*x, bg = c[3], pch = 21, add = T)
############################################
y  <- A + B/x + C * x;
############################################
matplot(x, y, type = "l", lwd = 2, col = c[9], add = T)
axis(1, pos = 0)
axis(2, pos = 0)